-- 1. 유저 중 나이가 30살이고 성별이 남자인 사람의 이름 조회
SELECT *
FROM TBL_BUY_USER
WHERE BUY_USER_AGE = 30 AND BUY_USER_GENDER = '남';

-- 2. 성별이 여자이면서 경기도에 살고 있는 사용자가 구매한 상품 목록 조회
SELECT TBP.*, TBU.ID
FROM TBL_BUY_USER TBU
JOIN TBL_PRODUCT_ORDER TBO
ON TBU.ID = TBO.BUY_USER_ID 
JOIN TBL_SELL_PRODUCT TBP
ON TBO.SELL_PRODUCT_ID = TBP.ID
WHERE BUY_USER_GENDER = '여' AND BUY_USER_ADDRESS LIKE '%경기도%';

-- 3. 브랜드 '무신사'를 선호하는 나이대 조회(만약에 동일하면 동일한 것을 모두 출력)
SELECT TBU.BUY_USER_AGE || '대' AS "무신사를 선호하는 나이대"
FROM TBL_PRODUCT_ORDER TBO
JOIN TBL_BUY_USER TBU
ON TBO.BUY_USER_ID = TBU.ID
WHERE SELL_PRODUCT_ID IN (
   SELECT ID
   FROM TBL_SELL_PRODUCT
   WHERE SELL_PRODUCT_BRAND = '무신사'
)
GROUP BY TBU.BUY_USER_AGE
ORDER BY COUNT(TBU.BUY_USER_AGE) DESC
FETCH FIRST 1 ROWS ONLY;

-- 4. 아디다스의 총 매출 조회
SELECT SUM(SELL_PRODUCT_PRICE) "아디다스의 총 매출"
FROM TBL_SELL_PRODUCT TBP
JOIN TBL_PRODUCT_ORDER TBO
ON TBP.ID = TBO.SELL_PRODUCT_ID 
WHERE SELL_PRODUCT_BRAND = '아디다스';

-- 5. 평균 재고보다 작은 상품 조회 
SELECT *
FROM TBL_SELL_PRODUCT 
WHERE SELL_PRODUCT_STOCK < (
   SELECT ROUND(AVG(SELL_PRODUCT_STOCK)) "PRODUCT_AVERAGE"
   FROM TBL_SELL_PRODUCT
);


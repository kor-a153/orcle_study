-- 컨버스 또는 예일 또는 버버리를 구매한 여성 이름 조회
SELECT DISTINCT TBU.BUY_USER_NAME
FROM TBL_PRODUCT_ORDER TBO
JOIN TBL_BUY_USER TBU
  ON TBU.ID = TBO.BUY_USER_ID
JOIN TBL_SELL_PRODUCT TBP
  ON TBP.ID = TBO.SELL_PRODUCT_ID
WHERE TBU.BUY_USER_GENDER = '여'
  AND (
    TBP.SELL_PRODUCT_BRAND LIKE '%컨버스%'
    OR TBP.SELL_PRODUCT_BRAND LIKE '%예일%'
    OR TBP.SELL_PRODUCT_BRAND LIKE '%버버리%'
  )
ORDER BY TBU.BUY_USER_NAME;
-- 20대가 가장 많이 구매한 상품
SELECT
    TBP.SELL_PRODUCT_NAME,
    COUNT(*) AS PURCHASE_COUNT
FROM TBL_PRODUCT_ORDER TBO
JOIN TBL_BUY_USER TBU
  ON TBU.ID = TBO.BUY_USER_ID
JOIN TBL_SELL_PRODUCT TBP
  ON TBP.ID = TBO.SELL_PRODUCT_ID
WHERE TBU.BUY_USER_AGE BETWEEN 20 AND 29
GROUP BY TBP.SELL_PRODUCT_NAME
HAVING COUNT(*) = (
    SELECT MAX(PURCHASE_COUNT)
    FROM (
        SELECT COUNT(*) AS PURCHASE_COUNT
        FROM TBL_PRODUCT_ORDER TBO2
        JOIN TBL_BUY_USER TBU2
          ON TBU2.ID = TBO2.BUY_USER_ID
        WHERE TBU2.BUY_USER_AGE BETWEEN 20 AND 29
        GROUP BY TBO2.SELL_PRODUCT_ID
    )
)
ORDER BY TBP.SELL_PRODUCT_NAME;
-- 최고 매출을 낸 상품 TOP5 조회
SELECT
    TBP.ID,
    TBP.SELL_PRODUCT_NAME,
    TBP.SELL_PRODUCT_BRAND,
    TBP.SELL_PRODUCT_PRICE,
    COUNT(*) AS ORDER_COUNT,
    COUNT(*) * TBP.SELL_PRODUCT_PRICE AS TOTAL_SALES
FROM TBL_PRODUCT_ORDER TBO
JOIN TBL_SELL_PRODUCT TBP
  ON TBP.ID = TBO.SELL_PRODUCT_ID
GROUP BY
    TBP.ID,
    TBP.SELL_PRODUCT_NAME,
    TBP.SELL_PRODUCT_BRAND,
    TBP.SELL_PRODUCT_PRICE
ORDER BY TOTAL_SALES DESC
FETCH FIRST 5 ROWS ONLY; -- 페치 퍼스트 문은 숫자를 잘 써주면 위에서 n개의 행을 출력함.

-- 가장 판매 실적이 저조한 상품 조회(판매실적 = 주문수량)
SELECT
    TBP.ID,
    TBP.SELL_PRODUCT_NAME,
    TBP.SELL_PRODUCT_BRAND,
    COUNT(TBO.ID) AS ORDER_COUNT
FROM TBL_SELL_PRODUCT TBP
LEFT JOIN TBL_PRODUCT_ORDER TBO
  ON TBO.SELL_PRODUCT_ID = TBP.ID
GROUP BY
    TBP.ID,
    TBP.SELL_PRODUCT_NAME,
    TBP.SELL_PRODUCT_BRAND
HAVING COUNT(TBO.ID) = (
    SELECT MIN(ORDER_COUNT)
    FROM (
        SELECT COUNT(TBO2.ID) AS ORDER_COUNT
        FROM TBL_SELL_PRODUCT TBP2
        LEFT JOIN TBL_PRODUCT_ORDER TBO2
          ON TBO2.SELL_PRODUCT_ID = TBP2.ID
        GROUP BY TBP2.ID
    )
)
ORDER BY TBP.ID;

-- 서울에 거주자의 1인당 평균 소비 지출액 조회
SELECT AVG(PER_USER_TOTAL_SALES) AS AVG_SALES_PER_PERSON_SEOUL
FROM (
    SELECT
        TBU.ID,
        SUM(TBP.SELL_PRODUCT_PRICE) AS PER_USER_TOTAL_SALES
    FROM TBL_BUY_USER TBU
    JOIN TBL_PRODUCT_ORDER TBO
      ON TBO.BUY_USER_ID = TBU.ID
    JOIN TBL_SELL_PRODUCT TBP
      ON TBP.ID = TBO.SELL_PRODUCT_ID
    WHERE TBU.BUY_USER_ADDRESS LIKE '%서울%'
    GROUP BY TBU.ID
);


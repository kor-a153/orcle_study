SELECT TBU.*
FROM TBL_BUY_USER TBU
JOIN (
   SELECT BUY_USER_ID 
   FROM TBL_PRODUCT_ORDER TBO
   WHERE ID = (
      SELECT SELL_PRODUCT_ID
      FROM TBL_PRODUCT_ORDER TBO
      GROUP BY SELL_PRODUCT_ID
      ORDER BY COUNT(SELL_PRODUCT_ID) DESC
      FETCH FIRST 1 ROWS ONLY
   )
